<template>
  <footer id="footer" :style="{ backgroundColor: curMenu.bgColor }">
    <div v-for="obj in menuList" :key="obj.path" @click="changeMenu(obj)">
      <router-link :to="obj.path">{{obj.name}}</router-link>
    </div>
  </footer>
</template>

<script>
import { mapState,mapMutations } from 'vuex';
export default {
  data(){
    return{
      menuList:[
        {
          path:'/',
          name:'剧集',
          bgColor:'#ff0000'
        },
        {
          path:'/music',
          name:'音乐',
          bgColor:'#ff00ff'
        },
        {
          path:'/book',
          name:'书籍',
          bgColor:'#cc0000'
        },
        {
          path:'/chat',
          name:'聊天',
          bgColor:'#eeb090'
        },
      ]
    }
  },
  computed:{
    ...mapState(['curMenu'])
  },
  methods: {
    ...mapMutations(['setCurMenu']),
    changeMenu(obj){
      this.setCurMenu(obj);
    },
  },
};
</script>

